package br.puc.mestrado.rebeca;

public class Gatilho {

	private double P;
	private double E;
	private double F;

	public Gatilho(double P, double E, double F) {
		this.P = P;
		this.E = E;
		this.F = F;
	}

	public double getP() {
		return P;
	}

	public void setP(double p) {
		P = p;
	}

	public double getE() {
		return E;
	}

	public void setE(double e) {
		E = e;
	}

	public double getF() {
		return F;
	}

	public void setF(double f) {
		F = f;
	}

}
